                    <p class="CONFERENCE-text">Healthcare-associated infections (HAIs) continues to be a significant concern for health care 
                    professionals. Centers for Disease Control and Prevention (CDC) reports that one in every 31
                    hospital patients acquires at least one HAI each day. This emphasizes the need for HAI 
                    preventive and control measures.
                   
