                    the topics related to HAIs.
                 
                    The aim of this two days event is to establish the importance of HAI mitigation with 
                    infection prevention and control practices and enhance the overall clinical outcomes 
                    for patients affected by HAIs.

                    We together as Infection Control Professionals, Epidemiologists, Microbiologists, Physicians, 
                    Nurses, Laboratory scientists, and other Medical Professionals need to come forward to 
